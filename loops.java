// For every first loop i the inner loop j will run n times

public class loops {
    public static void main(String[] args) {
        for(int i = 0; i <5 ; i++){
            for(int j= 0; j < 5 ; j++){
                System.out.print(" * ");
            }
            System.out.println(" "); // it will take one line space
        }
    }
}
